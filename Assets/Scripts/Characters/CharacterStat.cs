namespace MetaBalance.Characters
{
    /// <summary>
    /// Character stats enumeration
    /// </summary>
    public enum CharacterStat
    {
        Health,    // How much damage the character can take
        Damage,    // How much damage the character deals
        Speed,     // How fast the character moves
        Utility,   // The character's utility value (crowd control, healing, etc.)
        WinRate,   // The character's current win rate
        Popularity // How popular the character is among players
    }
}